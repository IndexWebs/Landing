<script>
import gsap from "gsap";
import { ScrollTrigger } from "gsap/ScrollTrigger";

export default {
    name: "JuntosIlustration",

    mounted() {
        // Se agrega el listener del mouse al montar el componente
        window.addEventListener("mousemove", this.handleMouseMove);
        gsap.to("#RuedaUno", {
            rotation: 360,
            transformOrigin: "center center", // Esto hace que la rotación sea desde la esquina superior izquierda
            duration: 4,
            repeat: -1,
            yoyo: true,
        });
        gsap.to("#RuedaDos", {
            rotation: -360,
            transformOrigin: "center center", // Esto hace que la rotación sea desde la esquina superior izquierda
            duration: 4,
            repeat: -1,
            yoyo: true,
        });
        this.registerGsap();
    },

    destroyed() {
        // Se elimina el listener del mouse cuando el componente se destruye
        window.removeEventListener("mousemove", this.handleMouseMove);
    },

    methods: {
        handleMouseMove(event) {
            const { clientY } = event;
            const yMove = (clientY / window.innerHeight - 0.5) * 20; // Movimiento en Y (-10 a 10)
            const maxRotation = 25;
            const minRotation = -25;

            const limitedRotation = Math.max(Math.min(yMove, maxRotation), minRotation);

            gsap.to("#Brazo", {
                rotation: limitedRotation,
                transformOrigin: "left bottom",
                duration: 0.1,
                ease: "power2.out",
            });
        },

        registerGsap() {
            gsap.registerPlugin(ScrollTrigger);
            this.scrollAnimation();
        },

        // Método para las animaciones de ScrollTrigger
        scrollAnimation() {
            gsap.timeline({
                scrollTrigger: {
                    trigger: ".box",
                    start: "top 80%",
                    end: "+=350",
                    markers: true,
                    scrub: 1,
                    pin: false,
                },
            })
                .from("#Arrow", {
                    scale: 0.5,  // Inicia con escala de altura 0
                    transformOrigin: "bottom",  // Crece desde la parte inferior
                    duration: 2
                });
        },
    },
};
